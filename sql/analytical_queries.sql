----Customer Profitability Ranking----

SELECT
    CUSTOMER_ID,
    CUSTOMER_NAME,
    SUM(PROFIT) AS TOTAL_PROFIT,
    RANK() OVER (
        ORDER BY SUM(PROFIT) DESC
    ) AS PROFIT_RANK
FROM STG_RETAIL_SALES
GROUP BY CUSTOMER_ID, CUSTOMER_NAME;
